    <h2>Стиль</h2>

    <p>Л. Заменгоф писал: «Истинный эсперантский стиль — стиль не славянский, не германский и не романский, а только стиль простой и логичный». Особенно важно следовать этому принципу для избегания бездумного дословного перевода и калек с родного языка, выходящих за рамки общепринятой нормы (именно такую норму отражают примеры и описывают комментарии в данном словаре). Что же касается деления самого языка на стили (высокий, низкий, газетный, просторечный и пр.), то различия такого рода в эсперанто выражены гораздо меньше, чем в национальных языках, в том числе в русском. Это объясняется, с одной стороны, молодостью языка, а с другой — большей однородностью эсперантистского сообщества по сравнению с сообществами национальными в плане образовательного уровня, а также отсутствием в эсперанто-движении некоторых социальных групп, свойственных этносам (криминальные элементы, маргиналы и пр.)<sup class="footnote" ><router-link :to="{name:'grammar',hash:'#sup1', params:{page:'15'}}">1</router-link></sup>. Тем не менее, стили постепенно складываются и в этом языке. К «классическому» эсперанто безусловно можно отнести произведения самого Л. Заменгофа, а также некоторых авторов первого периода, например Казимежа Бейна и Генриха Лойкена. Однако сейчас ряд слов и оборотов того времени перешли в разряд архаизмов. Позже огромный вклад в развитие литературной нормы внесли Каломан Калочай, Юлио Баги, Мэрджори Бултон, Уильям Олд, Рето Россетти и многие другие классики. Недостаточность стилевых различий в эсперанто некоторые авторы преодолевают с помощью намеренного их усиления или даже изобретения собственных стилистически окрашенных вариантов языка, например, для передачи средневековой речи, речи иностранцев, блатного жаргона или богемной беседы. В этом отношении очень интересны языковые эксперименты Каломана Калочая, Раймона Шварца, Лорьяка, Кароля Пича, Тревора Стила. Излишне напоминать, что для такого творчества необходим очень высокий уровень владения языком. В последнее время активно развивается молодёжный сленг. Он отличается каламбурами, обилием неологизмов (многие из которых не заимствованы из других языков, а созданы на базе эсперантского лексического материала), а также некоторыми грамматическими нововведениями. Но начинающим эсперантистам, прежде чем пользоваться ими, мы рекомендуем хорошо освоить литературную норму.</p>

