    <p>Однако в силу специфики данного понятия, а также вследствие совпадения форм числительного и существительного для него в национальных языках многие эсперантисты во всех случаях употребляют форму nulo.</p>

    <p>Порядковые числительные (ordaj, vicmontraj numeraloj) образуются путём прибавления окончания <strong>-a</strong> (а у количественных числительных, имеющих только форму существительного, — путём замены этим окончанием окончания -o) к простому или сложному количественному числительному или к последнему слову составного количественного числительного: nula «нулевой», unua «первый», dua «второй», tria «третий»… deka «десятый», dudeka «двадцатый», trideka «тридцатый»… centa «сотый», ducenta «двухсотый», tricenta «трёхсотый»… mila «тысячный», miliona «миллионный», miliarda «миллиардный».</p>

    <p>Форма на -a, как и все прилагательные, может принимать окончания множественного числа -j и винительного падежа -n.</p>

    <p>Путём прибавления к количественному числительному окончания <strong>-e</strong> образуются соответствующие наречия, которые в эсперанто нередко трактуются как наречные числительные (adverbaj numeraloj): unue «во-первых», due «во-вторых», trie «в-третьих» и т. д.</p>

    <p>Дробные числительные образуются при помощи суффикса <strong>-on-</strong>: duono «половина, одна вторая», triono «треть, одна третья», dekono «одна десятая», centono «одна сотая» и т. д.; du trionoj «две третьих», kvin seponoj «пять седьмых» и т. д.; duona «половинный», duone «наполовину, вполовину, пополам».</p>

    <p>Кратные числительные образуются при помощи суффикса <strong>-obl-</strong>: duobla «двойной, двукратный», triobla «тройной, троекратный», centobla «стократный» и т. д.; duoble «вдвойне, вдвое», centoble «стократно, в сто раз» и т. д.; duoblo «двойное количество, двойное число».</p>

    <p>Собирательные числительные образуются при помощи суффикса <strong>-op-</strong>: duopo «пара», triopo «трое», kvaropo «четверо» и т. д.; duopa «парный», triopa «в котором участвуют трое, состоящий из трёх единиц», kvaropa «состоящий из четырёх единиц» и т. д.; duope «вдвоём», triope «втроём», kvarope «вчетвером» и т. д.</p>

    <p>Правописание числительных подчиняется следующим правилам.</p>

