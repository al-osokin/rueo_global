    <p>Количественные числительные не склоняются: Mi donis al li unu libron «Я дал ему одну книгу»; Mi vidis tri virojn «Я видел трёх мужчин». Числительное unu «один» часто употребляется в значении местоимения — в единственном и множественном числе: Tiun libron donacis al mi unu el miaj amikoj «Эту книгу подарил мне один из моих друзей»; Unuj sidas, aliaj staras «Одни сидят, другие стоят» (подробнее об unu — см. статью в данном словаре). Путём прибавления к количественному числительному окончания <strong>-o</strong> образуются соответствующие существительные, которые в эсперанто нередко трактуются как субстантивные числительные (substantivaj numeraloj): unuo «единица», duo «двойка», trio «тройка», kvaro «четвёрка», kvino «пятёрка», seso «шестёрка», sepo «семёрка», oko «восьмёрка», naŭo «девятка», deko «десятка, десяток», cento «сотня», milo «тысяча».</p>

    <p>Форма на -o, как и все существительные, может принимать окончания множественного числа -j и винительного падежа -n. Как легко заметить, русское слово «тысяча» на эсперанто может переводиться двояко, в зависимости от смысла: mil okcent sesdek kvin «тысяча восемьсот шестьдесят пять»; stadiono por mil spektantoj «стадион на тысячу зрителей», но ankoraŭ unu milo da spektantoj «ещё одна тысяча зрителей».</p>

    <p>Как видно из последнего примера, с последующим существительным такие слова соединяются при помощи предлога <strong>da</strong>, выражающего родительный падеж количества.</p>

    <p>Наименования чисел miliono «миллион», miliardo «миллиард», biliono «биллион», triliono «триллион» и т. д. имеют форму только имени существительного.</p>

    <blockquote>
      <p>Поскольку числительные «биллион», «триллион» и некоторые другие имеют разное значение в зависимости от системы называний больших чисел (система с длинной шкалой и принятая в России система с короткой шкалой), в эсперанто был разработан основанный на системе с длинной шкалой способ образования названий чисел с помощью суффиксов -ilion- и -iliard- (см. соответствующие статьи в словаре). Однако с практической точки зрения нам представляется более удобным по возможности выражать такие числа через степень числа 10.</p>
    </blockquote>

    <p>Об обозначении понятия «ноль» следует сказать особо. «Фундаментальной» является форма существительного nulo; впоследствии Академия эсперанто официализировала форму количественного числительного nul. Ср.:</p>

    <table class="dtable">
