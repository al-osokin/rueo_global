    <h2>Числительные</h2>

    <p>Количественные числительные (bazaj numeraloj) бывают простые, сложные и составные.</p>

    <p>Простые числительные состоят из одного корня. К ним относятся наименования чисел от одного до десяти, а также чисел 100 и 1000: 1 — unu, 2 — du, 3 — tri, 4 — kvar, 5 — kvin, 6 — ses, 7 — sep, 8 — ok, 9 — naŭ, 10 — dek, 100 — cent, 1000 — mil.</p>

    <p>Сложные числительные имеют два корня. К ним относятся наименования чисел, обозначающих десятки от двадцати до девяноста и сотни от двухсот до девятисот:<br>
