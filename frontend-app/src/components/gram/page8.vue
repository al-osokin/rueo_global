    <p>Поскольку в эсперанто нет категории грамматического рода, местоимение ĝi «оно» относится ко всем неодушевлённым
      предметам и абстрактным понятиям, а также к одушевлённым предметам, т. е. живым существам, не обладающим сознанием
      (животным, маленьким детям), если нет необходимости специально указывать их пол. На русский язык ĝi переводится
      местоимениями «он», «она», «оно» в зависимости от грамматического рода русского слова: la tranĉilo tranĉas bone,
      ĉar ĝi estas akra «нож режет хорошо, потому что он острый»; Jen kuras hundo, ĝi estas granda «Вот бежит собака,
      она большая»; La infano ploras, ĉar ĝi volas manĝi «Ребёнок плачет, потому что он хочет есть», «Дитя плачет,
      потому что оно хочет есть». По отношению к животным ĝi обычно употребляется, даже если пол животного известен: Mi
      havas virkaton, mi tre amas ĝin «У меня есть кот, я очень люблю его».</p>

    <blockquote>
      <p>Отступления от последнего принципа делаются, если указание пола животного важно для смысла: Melku bovon
        senfine, li lakton ne donos «Сколько ни дои быка, не даёт он молока»; Mi kondukis kapron kun kaprino al foiro;
        subite ŝi haltis, dum li trotis plu «Я вёл козла с козой на ярмарку; вдруг она остановилась, а он семенил
        дальше». В сказках и баснях по отношению к животным, чей пол известен, также употребляются местоимения li и ŝi;
        они же употребляются и по отношению к персонифицированным объектам сказочных произведений, которым присвоен
        определённый пол.</p>
    </blockquote>

    <p>Ĝi может также заменять собой указательное местоимение «это» и, таким образом, переводиться как «это». Такое
      словоупотребление оправданно во избежание двойного употребления местоимения tio во фразах типа: Ĝi estas ĝuste
      tio, pri kio mi volis paroli kun vi «Это именно то, о чём я хотел поговорить с вами», а также если ĝi может быть
      равноценно употреблено и как личное местоимение: Jen estas kato. Ĝi estas ŝia kato «Вот кошка. Это её кошка»; Kio
      estas ĝi? — Ĝi estas pomo «Что это? — Это яблоко». В остальных случаях (например в характерных для раннего
      эсперанто фразах типа ĝi estas mi «это я» или mi venus, se mi trovus ĝin necesa «я пришёл бы, если бы я нашёл это
      необходимым») употребление ĝi в значении «это» воспринимается как архаизм. Основное значение ĝi всегда является
      приоритетным для недопущения возможной двусмысленности. Ср.: Maria skribis la leteron, mi vidis ĝin «Мария
      написала письмо, я видел его»; Maria skribis la leteron, mi vidis tion «Мария написала письмо, я видел это».</p>

    <p>Если речь идёт об обладающем сознанием существе, чей пол не определён, в эсперанто традиционно употребляется
      местоимение мужского рода li: Se iu rapidas hejmen, li povas foriri «Если кто-то торопится домой, он может уйти»;
      Esperantisto estas nomata ĉiu persono, kiu scias kaj uzas la lingvon Esperanto tute egale por kiaj celoj li ĝin
      uzas «Эсперантистом называется любая личность, которая знает и использует язык эсперанто, всё равно для каких
      целей она его использует».</p>

    <blockquote>
      <p>Многие эсперантисты считают употребление в таких случаях li нелогичным и предлагают различные варианты решения
        проблемы. Из всех предложенных для этой цели местоимений стоит упомянуть местоимения ŝli и ri (см.
        соответствующие статьи в словаре). Однако эти и им подобные формы распространения не получили.</p>
    </blockquote>

    <p>Множественное число для местоимений li, ŝi, ĝi общее, как и в русском языке: ili «они».</p>

    <blockquote>
      <p>Некоторые новаторы предлагают ввести формы iŝi и iĝi для употребления в случаях, если речь идёт только о
        существах женского пола или о неодушевлённых предметах и абстрактных понятиях. Эти формы также не получили
        распространения.</p>
    </blockquote>

    <p>В эсперанто существует также неопределённо-личное местоимение (nedifina persona pronomo) <strong>oni</strong>,
      аналогичное французскому on или немецкому man<sup class="footnote"><router-link
          :to="{ name: 'grammar', hash: '#sup2', params: { page: '8' } }">2</router-link></sup>. На русский язык оно
      дословно не переводится. Обычно оно соответствует 3-му лицу множественного числа, иногда — безличному обороту:
      oni skribas «пишут», oni devas «до́лжно».</p>

    <p>Склоняются личные местоимения так же, как существительные. Винительный падеж у них образуется прибавлением
      окончания <strong>-n</strong> к форме именительного падежа: vidi min «видеть меня», koni lin «знать его», viziti
      ilin «посетить их». Аналогично для неопределённо-личного местоимения oni: Oni parolu, mi onin ne aŭskultas! «Пусть
      говорят, я (их) не слушаю!»</p>

    <p>Остальные падежи передаются с помощью предлогов: ricevi de mi «получить от меня», skribita de mi «написанный
      мной», iri al mi «идти ко мне», fieri pri mi «гордиться мной», sen mi «без меня» и т. п.</p>

    <p>Притяжательные местоимения (posedaj pronomoj<sup class="footnote"><router-link
          :to="{ name: 'grammar', hash: '#sup3', params: { page: '8' } }">3</router-link></sup>, posesivoj) образуются
      путём
      добавления окончания <strong>-a</strong> к личному местоимению: mia «мой», via «твой, ваш», lia «его», ŝia «её»,
      ilia «их» и т. д. Подобно прилагательным, притяжательные местоимения могут принимать окончания множественного
      числа <strong>-j</strong> и винительного падежа <strong>-n</strong>. При этом, в отличие от русского языка, они
      всегда согласуются в числе с существительными и прилагательными, к которым относятся. А поскольку в русском языке
      личное местоимение третьего лица в винительном падеже и образованное от него притяжательное местоимение во всех
      падежах, в частности в именительном и винительном, к тому же имеют одну форму, правильное употребление окончаний в
      соответствующих эсперантских формах нередко представляет большие трудности. Ср.:</p>

