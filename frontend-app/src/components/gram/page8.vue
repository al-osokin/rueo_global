    <p>То же в следующей фразе: Mi trovis <strong>ŝin</strong> kaj <strong>ŝiajn</strong> gepatrojn «Я нашёл её и её
      родителей».</p>

    <p>В эсперанто имеется возвратное местоимение 3-го лица (refleksiva pronomo, refleksivo) <strong>si</strong>.
      Именительный падеж данного слова на русский язык не переводится, а употребляется оно в этой форме практически
      только с предлогами: de si «от себя», al si «к себе», pri si «о себе» и др. Самостоятельно в роли существительного
      si употребляется лишь в особых случаях (соответственно только в этих случаях si может выступать во фразах в
      качестве подлежащего): Si estas la ŝlosilo de la homa koro «Самость — ключ к человеческому сердцу», «Собственное
      „я“ — ключ к человеческому сердцу». Как и от личных местоимений, от si можно образовать форму винительного падежа
      sin «себя» и возвратно-притяжательное местоимение sia «свой» (которое может употребляться во множественном числе и
      винительном падеже). Форма sin является элементом возвратных глаголов, выражающих сознательное действие объекта,
      направленное на себя: vesti sin «одеваться», razi sin «бриться». При этом, даже будучи поставлена перед глаголом,
      эта форма не должна превращаться в приставку и на письме не должна сливаться с глаголом: sin vesti, sin razi.</p>

    <p>Возвратное местоимение si и возвратно-притяжательное местоимение sia употребляются исключительно для обозначения
      3-го лица единственного и множественного числа. В остальных лицах, в отличие от русского языка, употребляются
      только соответствующие личные и притяжательные местоимения:<br>
