    <p>Фраза li donas al li переводилась бы как «он даёт ему» (<em>т. е. другому человеку</em>), фраза li kombas lin
      означала бы «он причёсывает его (<em>т. е. другого человека</em>), а фраза li legas lian libron звучала бы на
      русском языке как «он читает его книгу» (<em>т. е. книгу другого человека</em>).</p>

    <p>Если обращение сопровождается упоминанием объекта обращения в третьем лице, то, в случае указания на его
      обладание чем-либо, могут употребляться sia или via в зависимости от степени самостоятельности того, чем он
      обладает: Ho civitano, kiu forgesis sian devon! «О гражданин, который забыл свой долг!»; Ho vi Diano, kiu vian
      fraton amegas…» «О ты, Диана, которая очень любит своего брата…»</p>

    <blockquote>
      <p>Если не учитывать целый ряд уточнений и оговорок, не приводимых здесь из-за их объёма, правила употребления
        возвратного и возвратно-притяжательного местоимений в современном эсперанто можно сформулировать следующим
        образом: si и sia относятся к субъекту ближайшего выраженного действия. Действие может быть выражено глагольными
        формами или отглагольным существительным. Ср.: <br>
      </p><br>
      <table class="dtable">
        <tbody>
