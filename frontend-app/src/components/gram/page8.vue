          учителя</em>). (Когда субъект действия, выраженного инфинитивом, является объектом другого действия, но при
        этом сам не выражен, в современном языке он не принимается в расчёт. В ранних текстах это положение часто не
        соблюдалось.)</p>
    </blockquote>

    <blockquote>
      <p>В сочетаниях en si, per si (mem), inter si, если они являются устойчивыми оборотами, si теряет свою связь с
        субъектом действия: Oni ne povas ekkoni la realon en si mem «Невозможно познать реальность в себе» (<em>т. е.
          такой, какая она есть в действительности</em>); Ni komprenis liajn gestojn per si mem «Мы понимали его жесты
        сами по себе» (<em>т. е. непосредственно, без пояснений</em>); Tiujn malsamajn objektojn miksi inter si oni
        neniel devas «Эти разные предметы смешивать друг с другом никак нельзя». Но: Ili dividis la monon inter si «Они
        поделили деньги между собой».</p>
    </blockquote>

    <blockquote>
      <p>Sia, употреблённое в роли прилагательного в значении sia propra «(свой) собственный», также теряет связь с
        субъектом какого-либо действия: Li metis ĉion sur sian lokon «Он положил всё на своё место».</p>
    </blockquote>

    <p>Как приставка, употребляемая при образовании отглагольных существительных, прилагательных и наречий, форма sin
      относится уже ко всем лицам: Mia sindefendo «Моя самооборона»; Vi devas esti singardaj «Вы должны быть
      осмотрительными»; Ni agis singarde «Мы действовали осмотрительно». Форма же sia в качестве приставки продолжает
      относиться только к третьему лицу: Siaflanke li diris, ke… «Со своей стороны он сказал, что…»; Miaflanke mi povas
      diri, ke… «Со своей стороны я могу сказать, что…» (В слове siatempe «в своё время», т. е. «в подходящее время» или
      «в прежнее время», приставка sia потеряла своё первоначальное значение и, следовательно, жёсткую связь с третьим
      лицом: Mi faros tion siatempe «Я сделаю это в своё время».)</p>

