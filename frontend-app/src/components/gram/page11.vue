    <p>В некоторых случаях выделение слова с помощью артикля может повлиять на порядок слов:<br>
    </p><br><table class="dtable">
    <tbody><tr>
      <td> Petro venis kun malgaja vizaĝo «Пётр пришёл с грустным лицом». (Здесь речь идёт не столько о лице как части тела, сколько о выражении лица, настроении, и слово vizaĝo можно было бы заменить словами mieno или humoro; это существительное не несёт на себе логического акцента, поэтому стоит после определяющего его прилагательного malgaja.) </td>
    </tr>
    <tr>
      <td> Petro venis kun la vizaĝo malgaja «Пётр пришёл с грустным лицом». (Здесь речь идёт о том, что настроение Петра было сразу видно именно по его лицу; это существительное несёт на себе логический акцент, поэтому стоит перед определяющим его прилагательным malgaja.) </td>
    </tr>
    </tbody></table><p></p>


    <p>Приведённые нами случаи употребления и неупотребления артикля являются наиболее частыми и показательными, но не исчерпывающими всё их многообразие. Да и само деление на эти пункты довольно условно: некоторые фразы можно отнести сразу к двум и даже более случаям. Например, в сочетании la alveno de printempo «приход весны, наступление весны» артикль может выражать как абстрактность (если речь идёт о любом из случаев этого ежегодно повторяющегося явления), так и конкретность (если имеется в виду определённое событие в каком-то году), но в обоих случаях он указывает на определённость этого действия как совершённого данным деятелем; в сочетании la vivo de Zamenhof «жизнь Заменгофа» артикль, во-первых, указывает на определённость понятия «жизнь» (показывая, что оно относится к данному лицу), во-вторых, выражает уникальность этого понятия (поскольку у данного лица только одна жизнь) и, в-третьих, выделяет это понятие из ряда подобных (подчёркивая, что речь идёт именно о жизни, а не о рождении или смерти данного лица)<sup class="footnote" ><router-link :to="{name:'grammar',hash:'#sup5', params:{page:'11'}}">5</router-link></sup>. Кроме того, в каждом пункте можно было бы выделить ещё целый ряд подпунктов, нюансов и оговорок. Для того же, чтобы объяснить данную тему во всех подробностях, потребовалась бы целая книга. Желающим получить дополнительные сведения об артикле мы советуем обратиться к <span class="caps">PMEG</span> и <span class="caps">PAG</span>, а также к соответствующей главе в сборнике статей С. Б. Покровского «Lingvaj respondoj». Если же после всех объяснений остаются неясности, можно воспользоваться рекомендацией самого Л. Заменгофа: если вы колеблетесь в постановке артикля, лучше его не употребить, чем употребить неправильно (это не означает, что артикль можно вообще не употреблять; лучше его не поставить там, где он должен стоять, чем поставить там, где он стоять не должен).<br>
